# How works cookie (easy example):
# Scanning user data
username = input("Username: ")
password  = input("Password: ")
# Infinity commands logic
while True:
  # Scanner for commands
    cmd = input("Enter Command: ")
  #logic (for check username)
    if cmd == "username":
        print(username)
    elif cmd == "password":
      #logic (for check password)
        print(password)
      #logic (for exit)
    elif cmd == "quit":
        break
